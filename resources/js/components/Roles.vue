<script setup>

import { ref , onMounted , computed, watch} from 'vue';
import axios from 'axios';
import { useToastr } from '@/toastr';

const formData = ref({
  name: '',
  description: ''
});
const toastr = useToastr();
const isModalOpen = ref(false);

const openModal = () => {
  isModalOpen.value = true;
};

const closeModal = () => {
  isModalOpen.value = false;
};

const validateForm = () => {
  if (!formData.value.name || !formData.value.description) {
    toastr.error('Veuillez remplir tous les champs requis');
    return false;
  }
  return true;
};

const createRole = () => {
  if (!validateForm()) {
    return;
  }
  axios.post('/api/create-roles', formData.value)
    .then(response => {
      toastr.success('Role créé avec succès!');
      closeModal();
      fetchRoles();

      formData.value.name = '';
      formData.value.description = '';
    })
    .catch(error => {
      console.error('Erreur lors de la création du role:', error);
    });
};

const roles = ref([]);

const fetchRoles = async () => {
  try {
    const response = await axios.get('/api/getroles');
    roles.value = response.data;
  } catch (error) {
    console.error('Error fetching roles:', error);
  }
};

onMounted(() => {
  fetchRoles();
});

const searchQuery = ref('');


const filteredRoles = computed(() => {
  return roles.value.filter(role => {
    return (
      role.name.toLowerCase().includes(searchQuery.value.toLowerCase()) ||
      role.description.toLowerCase().includes(searchQuery.value.toLowerCase())
    );
  });
});

const itemsPerPage = 6;
let currentPage = ref(1);

const calculatePagination = () => {
  const startIndex = (currentPage.value - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  return { startIndex, endIndex };
};

const getPaginatedRoles = () => {
  const { startIndex, endIndex } = calculatePagination();
  return filteredRoles.value.slice(startIndex, endIndex);
};

const paginatedRoles = computed(getPaginatedRoles);

const prevPage = () => {
  if (currentPage.value > 1) {
    currentPage.value--;
  }
};

const nextPage = () => {
  if (currentPage.value < totalPages.value) {
    currentPage.value++;
  }
};

const totalPages = computed(() => Math.ceil(filteredRoles.value.length / itemsPerPage));




watch(searchQuery, () => {
  currentPage.value = 1;
});

</script>



